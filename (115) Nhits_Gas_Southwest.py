# Boundary Smoothing Functions
# -------------------------------------------
def apply_boundary_smoothing(df_pred, hist_stats, date_col, pred_cols,
                           smooth_hours=6, anomaly_threshold=2.0):
    """Apply intelligent smoothing based on historical patterns for multiple targets"""

    if df_pred.empty:
        return df_pred

    df_pred = df_pred.sort_values(date_col).reset_index(drop=True)

    for pred_col in pred_cols:
        if pred_col not in df_pred.columns or pred_col not in hist_stats:
            continue

        stats = hist_stats[pred_col]

        # Check for boundary anomaly
        first_pred = df_pred.iloc[0][pred_col]
        last_hist = stats['last_value']
        jump = first_pred - last_hist
        jump_z_score = abs(jump) / stats['std'] if stats['std'] > 0 else 0

        print(f"\n{pred_col} - Boundary jump: {jump:.4f} (z-score: {jump_z_score:.2f})")

        if jump_z_score > anomaly_threshold:
            print(f"Applying boundary smoothing (jump exceeds {anomaly_threshold} std devs)")

            # Exponential smoothing of the jump
            for i in range(min(smooth_hours, len(df_pred))):
                decay = np.exp(-i / (smooth_hours / 3))
                adjustment = jump * decay
                df_pred.loc[i, pred_col] = df_pred.loc[i, pred_col] - adjustment

            # Also apply confidence-based clipping for first 24 hours
            for i in range(min(24, len(df_pred))):
                confidence = i / 24  # 0 to 1 over 24 hours

                # For ratios, we have natural bounds [0, 1]
                max_deviation = stats['std'] * (anomaly_threshold + confidence * 2)
                upper_bound = min(1.0, last_hist + max_deviation)
                lower_bound = max(0.0, last_hist - max_deviation)

                # Add trend component to bounds if available
                if 'recent_trend' in stats and stats['recent_trend'] is not None:
                    trend_component = stats['recent_trend'] * (i + 1)
                    upper_bound = min(1.0, upper_bound + max(0, trend_component))
                    lower_bound = max(0.0, lower_bound + min(0, trend_component))

                # Store original value for comparison
                original = df_pred.loc[i, pred_col]
                clipped = np.clip(original, lower_bound, upper_bound)

                if original != clipped:
                    print(f"  Hour {i}: Clipped {original:.4f} to {clipped:.4f}")
                    df_pred.loc[i, pred_col] = clipped

    return df_pred

