# Enhanced Inference Function with Direct Overwrite (MODIFIED)
# -------------------------------------------
def do_inference_seamless_and_overwrite(
    model_to_use: NHiTSModel,
    train_file_path: str,
    predict_file_path: str,
    path_to_sklearn_scalers: str,
    path_to_scaler_info: str,
    target_cols_list: list,
    expected_covariate_cols: list,
    date_col_name: str = "date",
    bridge_hours: int = 168,
    boundary_smooth_hours: int = 6,
    anomaly_threshold: float = 2.0,
    min_year_train: int = None,
    time_feature_cols: list = TIME_FEATURE_COLS,
    backup_predict_file: bool = True,
    reference_output_path: str = None
):
    """
    Perform inference using seamless data bridging and OVERWRITE the predict.parquet file.
    """
    print(f"\n--- Starting Enhanced Inference with Direct Overwrite ---")
    start_time = time.time()
    
    # ### NEW: Create backup of predict.parquet before modifying ###
    if backup_predict_file and os.path.exists(predict_file_path):
        backup_path = predict_file_path.replace('.parquet', '_backup_gas.parquet')
        print(f"Creating backup of predict.parquet at: {backup_path}")
        try:
            import shutil
            shutil.copy2(predict_file_path, backup_path)
            print("Backup created successfully.")
        except Exception as e:
            print(f"WARNING: Could not create backup: {e}")

    # Step 1: Load the original predict.parquet to preserve all columns
    print(f"\nLoading original predict.parquet from: {predict_file_path}")
    df_predict_original = pd.read_parquet(predict_file_path)
    df_predict_original[date_col_name] = pd.to_datetime(df_predict_original[date_col_name])
    print(f"Original predict.parquet shape: {df_predict_original.shape}")
    print(f"Target columns to be replaced: {target_cols_list}")

    # Step 2: Create seamless dataset (passing features_to_remove)
    df_seamless, actual_forecast_start = create_seamless_prediction_data(
        train_file_path=train_file_path,
        predict_file_path=predict_file_path,
        target_cols=target_cols_list,
        date_col=date_col_name,
        bridge_hours=bridge_hours,
        features_to_remove=features_to_remove  # Pass the global features_to_remove list
    )

    # Step 3: Load scalers and info
    local_cov_scalers_dict = {}
    scaler_info = None

    if path_to_sklearn_scalers and os.path.exists(path_to_sklearn_scalers):
        try:
            local_cov_scalers_dict = joblib.load(path_to_sklearn_scalers)
            print("Covariate scalers dict loaded.")
        except Exception as e:
            print(f"Warn: Failed load cov scalers: {e}")
    else:
        print("Warn: Covariate scalers path missing or not found. Covariates will not be scaled.")

    if path_to_scaler_info and os.path.exists(path_to_scaler_info):
        try:
            scaler_info = joblib.load(path_to_scaler_info)
            print("Scaler info loaded.")
            # Check if the saved model used the same feature removal
            if 'features_removed' in scaler_info:
                saved_features_removed = scaler_info['features_removed']
                if set(saved_features_removed) != set(features_to_remove):
                    print("WARNING: Feature removal list differs from what was used in training!")
                    print(f"  Training removed: {len(saved_features_removed)} features")
                    print(f"  Current list has: {len(features_to_remove)} features")
        except Exception as e:
            print(f"Warning: Failed to load scaler info: {e}")

    # Step 4: Prepare features for seamless data
    # Remove features from seamless data based on removal list
    cols_to_drop = [col for col in features_to_remove if col in df_seamless.columns]
    if cols_to_drop:
        print(f"Removing {len(cols_to_drop)} features from seamless data based on removal list")
        df_seamless = df_seamless.drop(columns=cols_to_drop)

    # Generate any missing features (that are not in the removal list)
    missing_cols = [c for c in expected_covariate_cols if c not in df_seamless.columns]
    if missing_cols:
        print(f"Generating missing features: {len(missing_cols)} features")
        time_features_to_gen = [tf for tf in time_feature_cols if tf in missing_cols]
        if time_features_to_gen:
            print("Regenerating time features...")
            df_seamless, _ = create_time_features(df_seamless, date_col_name, min_year_train)

        # Remove any generated features that are in the removal list
        cols_to_drop_after_gen = [col for col in features_to_remove if col in df_seamless.columns]
        if cols_to_drop_after_gen:
            df_seamless = df_seamless.drop(columns=cols_to_drop_after_gen)

        # Check again
        still_missing = [c for c in expected_covariate_cols if c not in df_seamless.columns]
        if still_missing:
            print(f"Warning: Still missing non-time covariates: {len(still_missing)} columns. Filling with 0.")
            print(f"  Sample of missing: {still_missing[:5]}")
            for col in still_missing:
                df_seamless[col] = 0

    # Fill any NaNs in covariates
    if expected_covariate_cols:
        # Only fill NaNs for columns that exist and are expected
        existing_expected_cols = [c for c in expected_covariate_cols if c in df_seamless.columns]
        nan_counts = df_seamless[existing_expected_cols].isnull().sum()
        if nan_counts.sum() > 0:
            print("Filling NaNs in covariates...")
            df_seamless[existing_expected_cols] = df_seamless[existing_expected_cols].ffill().bfill().fillna(0)

    # Step 5: Split seamless data into historical and future
    df_historical = df_seamless[df_seamless[date_col_name] < actual_forecast_start].copy()
    df_future = df_seamless[df_seamless[date_col_name] >= actual_forecast_start].copy()

    print(f"\nSplit seamless data at {actual_forecast_start}:")
    print(f"  - Historical (with bridge): {len(df_historical)} hours")
    print(f"  - Future to predict: {len(df_future)} hours")

    # Step 6: Create TimeSeries objects
    # Historical targets (includes bridge data) - NO SCALING for targets
    hist_target_unscaled = None
    if all(col in df_historical.columns for col in target_cols_list):
        hist_target_unscaled = TimeSeries.from_dataframe(
            df_historical, date_col_name, target_cols_list, freq='h'
        )
        print(f"Historical target TimeSeries created: {len(hist_target_unscaled)} timesteps")

    # Covariates for entire span (historical + future)
    seamless_cov_scaled = None
    if expected_covariate_cols:
        # Ensure column order matches training
        cols_for_ts = [c for c in expected_covariate_cols if c in df_seamless.columns]

        if cols_for_ts:
            seamless_cov_ts = TimeSeries.from_dataframe(
                df_seamless, date_col_name, cols_for_ts, freq='h',
                fill_missing_dates=True, fillna_value=0
            )

            # Scale covariates if scaler available
            if 'covariates' in local_cov_scalers_dict:
                print("Scaling seamless covariates...")
                try:
                    darts_cov_scaler_inf = DartsScaler(scaler=local_cov_scalers_dict['covariates'], name="StdScalerCovInf", n_jobs=-1)
                    darts_cov_scaler_inf.fit(seamless_cov_ts)
                    seamless_cov_scaled = darts_cov_scaler_inf.transform(seamless_cov_ts)
                    print(f"Scaled seamless covariates: {seamless_cov_scaled.width} components, {len(seamless_cov_scaled)} timesteps")
                except Exception as e:
                    print(f"Error scaling seamless covariates: {e}")
                    seamless_cov_scaled = seamless_cov_ts
            else:
                print("No covariate scaler available, using unscaled covariates")
                seamless_cov_scaled = seamless_cov_ts

    # Step 7: Calculate historical statistics for boundary checking
    hist_stats = {}
    for target_col in target_cols_list:
        if target_col in df_historical.columns:
            # Get last week of historical data
            week_ago = actual_forecast_start - pd.Timedelta(hours=168)
            recent_history = df_historical[df_historical[date_col_name] >= week_ago][target_col]

            if len(recent_history) > 0:
                hist_stats[target_col] = {
                    'mean': recent_history.mean(),
                    'std': recent_history.std(),
                    'last_value': recent_history.iloc[-1],
                    'recent_trend': np.mean(np.diff(recent_history.tail(24))) if len(recent_history) >= 24 else 0
                }
                print(f"\nHistorical stats for {target_col} from bridge data:")
                print(f"  Mean: {hist_stats[target_col]['mean']:.4f}")
                print(f"  Std: {hist_stats[target_col]['std']:.4f}")
                print(f"  Last value: {hist_stats[target_col]['last_value']:.4f}")
                print(f"  Recent trend: {hist_stats[target_col]['recent_trend']:.6f}/hour")

    # Step 8: Generate predictions
    n_predict = len(df_future)
    print(f"\nGenerating {n_predict} predictions...")

    predictions_unscaled = None
    try:
        # Prepare prediction inputs
        predict_kwargs = {'n': n_predict, 'verbose': True}

        # Add historical series if needed
        if model_to_use.input_chunk_length > 0:
            if hist_target_unscaled is None:
                raise ValueError("Model requires historical target data but none available")

            # Get the right chunk of history for prediction start
            hist_end = actual_forecast_start - pd.Timedelta(hours=1)
            hist_start = hist_end - pd.Timedelta(hours=model_to_use.input_chunk_length - 1)

            predict_series = hist_target_unscaled.slice(hist_start, hist_end)
            if len(predict_series) != model_to_use.input_chunk_length:
                raise ValueError(f"Historical chunk wrong size: {len(predict_series)} vs {model_to_use.input_chunk_length}")

            predict_kwargs['series'] = predict_series

        # Add covariates if available
        if seamless_cov_scaled is not None:
            predict_kwargs['past_covariates'] = seamless_cov_scaled

        # Generate predictions
        predictions_unscaled = model_to_use.predict(**predict_kwargs)

    except Exception as e:
        print(f"ERROR during prediction: {e}")
        traceback.print_exc()
        return df_predict_original  # Return original if prediction fails

    # Step 9: Post-process predictions
    if predictions_unscaled is None:
        print("ERROR: No predictions generated")
        return df_predict_original

    # Convert to dataframe
    df_pred_raw = predictions_unscaled.to_dataframe().reset_index()
    df_pred_raw.rename(columns={"time": date_col_name}, inplace=True)
    df_pred_raw[date_col_name] = pd.to_datetime(df_pred_raw[date_col_name])

    print("  (Skipping growth bump for ratio predictions)")

    # Keep only the predicted values for target columns
    df_predictions = df_pred_raw[[date_col_name] + target_cols_list].copy()

    # Clip predictions to [0, 1] for ratios
    for col in target_cols_list:
        if col in df_predictions.columns and "_RATIO" in col.upper():
            print(f"Clipping {col} to [0, 1]")
            df_predictions[col] = np.clip(df_predictions[col], 0, 1)

    # Step 10: Apply boundary smoothing if needed
    if hist_stats and boundary_smooth_hours > 0 and BOUNDARY_CONFIG['enabled']:
        print("\nChecking for boundary anomalies...")
        df_predictions = apply_boundary_smoothing(
            df_predictions, hist_stats, date_col_name,
            target_cols_list, boundary_smooth_hours, anomaly_threshold
        )

    # Step 11: OVERWRITE target columns in original predict.parquet
    print(f"\n--- Overwriting target columns in predict.parquet ---")
    
    # Merge predictions with original data
    df_updated = df_predict_original.copy()
    
    # Drop the old target columns from the updated dataframe
    for target_col in target_cols_list:
        if target_col in df_updated.columns:
            print(f"Dropping old column: {target_col}")
            df_updated = df_updated.drop(columns=[target_col])
    
    # Merge in the new predictions
    df_updated = pd.merge(df_updated, df_predictions, on=date_col_name, how='left')
    
    # Verify the merge
    print(f"Updated dataframe shape: {df_updated.shape}")
    print(f"Original dataframe shape: {df_predict_original.shape}")
    
    # Check that all target columns have been updated
    for target_col in target_cols_list:
        if target_col not in df_updated.columns:
            print(f"WARNING: Target column {target_col} not in updated dataframe!")
        else:
            nan_count = df_updated[target_col].isna().sum()
            print(f"  {target_col}: {len(df_updated) - nan_count}/{len(df_updated)} values filled")

    # Step 12: Save the updated dataframe back to predict.parquet
    print(f"\nOverwriting predict.parquet at: {predict_file_path}")
    try:
        df_updated.to_parquet(predict_file_path, index=False)
        print("Successfully overwrote predict.parquet with new predictions!")
    except Exception as e:
        print(f"ERROR: Could not save updated predict.parquet: {e}")
        return df_predict_original

    # Step 13: Optionally save a reference copy for debugging
    if reference_output_path:
        print(f"\nSaving reference copy to: {reference_output_path}")
        try:
            df_predictions.to_parquet(reference_output_path, index=False)
            print("Reference copy saved successfully.")
        except Exception as e:
            print(f"Warning: Could not save reference copy: {e}")

    end_time = time.time()
    print(f"\nInference and overwrite completed in {end_time - start_time:.2f} seconds")

    return df_updated

